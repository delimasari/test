1. Halaman Enkripsi : 
Digunakan untuk meng-enkripsi file dokumen .doc dan .xls
Berikut ini langkah-langkahnya
- Pilih Menu Enkripsi pada Form Menu 
- Pilih File yang akan di enkripsi
- Masukan Password yang di inginkan
- Klik tombol Enkripsi File
- Jika sudah muncul keterangan enkripsi file berhasil
- klik tombol unduh file untuk menentukan direktori file akan disimpan
- klik tombol cancel untuk kembali ke halaman enkripsi
						
2. Halaman Dekripsi :
Digunakan untuk men-dekripsi file dokumen .doc dan .xls
yang sudah di enkrip melalui aplikasi ini 
Berikut ini langkah-langkahnya
- Pilih Menu Dekripsi pada Form Menu 
- Pilih File Enkripsi yang akan di di dekripsi 
- Masukan Password yang sama dengan password file enkripsi 
- Klik tombol Dekripsi File
- Jika sudah muncul keterangan dekripsi file berhasil
- klik tombol unduh file untuk menentukan direktori file akan disimpan
- klik tombol cancel untuk kembali ke halaman dekripsi
